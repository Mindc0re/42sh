#include "../includes/sh21.h"

void		nav_to_delete_redir(char *str, int *i, int *j)
{
	while (str[(*i)] && str[(*i)] != ' ')
		(*i)--;
	(*j) = (*i);
	while (str[(*j)] && str[(*j) - 1] != '>')
		(*j)++;
	if (str[(*j)] == '>')
		(*j)++;
	while (str[(*j)] && str[(*j)] == ' ')
		(*j)++;
	while (str[(*j)] && str[(*j)] != ' ')
		(*j)++;
}
